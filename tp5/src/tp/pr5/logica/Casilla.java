package tp.pr5.logica;

public class Casilla {
private int x;
private int y;


	public Casilla(){
	
	this.x = 0;
	this.y = 0;
	
	
	}


	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}

	
	

}
